public class DataTest {

    public String id;
    public String str;

    public DataTest() {
    }

    public DataTest(String id, String str) {
        this.id = id;
        this.str = str;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }
}
